/**
 * add - adds two numbers
 * @a: first parameter
 * @b: second parameter
 *
 * return - sum
 */

int add(int a, int b)
{
	return a + b;
}

/**
 * sub - subtracts two numbers
 * @a: first parameter
 * @b: second parameter
 *
 * return - sub
 */

int sub(int a, int b)
{
        return a - b;
}

/**
 * mul - multiplys two numbers
 * @a: first parameter
 * @b: second parameter
 *
 * return - mul
 */

int mul(int a, int b)
{
        return a * b;
}

/**
 * div - divides two numbers
 * @a: first parameter
 * @b: second parameter
 *
 * return - div
 */

int div(int a, int b)
{
        return a / b;
}

/**
 * mod - reminder of two numbers
 * @a: first parameter
 * @b: second parameter
 *
 * return - mod
 */


int mod(int a, int b)
{
        return a % b;
}
